class Student:
    def __init__(self, first_name, last_name, idnumber, password, account_type, gender, dob, phone, email):
        self.first_name = first_name
        self.last_name = last_name
        self.idnumber = idnumber
        self.password = password
        self.account_type = account_type
        self.gender = gender
        self.dob = dob
        self.phone = phone
        self.email = email